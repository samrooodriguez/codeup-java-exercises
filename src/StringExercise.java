public class StringExercise {
    public static void main(String[] args){

        String message = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.print(message);

        message = message + "world! \n";

        System.out.println(message);

        message = message + "Check \"this\" out, \"s inside of \"s! \n";

        System.out.println(message);

        message = message + "In windows, the main drive is usually C:\\ \n";

        System.out.println(message);

        message = message + "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\! \n";

        System.out.println(message);
    }
}
